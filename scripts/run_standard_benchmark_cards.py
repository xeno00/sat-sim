"""Run bounded normalized standard benchmark cards.

Default behavior is plan-only. Use ``--run`` to execute implemented adapters
for one standard case and write non-final benchmark-card artifacts.
"""

from __future__ import annotations

import argparse
import csv
import json
import os
import shutil
import sys
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

SAT_SIM_ROOT = Path(__file__).resolve().parents[1]
if str(SAT_SIM_ROOT) not in sys.path:
    sys.path.insert(0, str(SAT_SIM_ROOT))

from jcls_sim.benchmark.runner import (  # noqa: E402
    benchmark_card_row,
    plan_rows,
    run_benchmark_cards,
    summary_row,
)
from jcls_sim.benchmark.standard_cases import PRIMARY_STANDARD_CASE_ID  # noqa: E402
from jcls_sim.io import json_ready  # noqa: E402


DEFAULT_OUTPUT_ROOT = SAT_SIM_ROOT / "outputs" / "standard_benchmark_cards"
REPORT_ROOT = SAT_SIM_ROOT / "outputs" / "reports"
NON_FINAL_FLAGS = {
    "artifact_status": "non_final_standard_benchmark_card",
    "benchmark_card": True,
    "manuscript_ready": False,
    "not_for_manuscript_submission": True,
    "final_figure": False,
}


def utc_now() -> str:
    """Return current UTC timestamp."""

    return datetime.now(timezone.utc).isoformat()


def repo_rel(path: Path) -> str:
    """Return a sat-sim relative path."""

    return path.resolve().relative_to(SAT_SIM_ROOT.resolve()).as_posix()


def reports_rel(path: Path) -> str:
    """Return a path relative to the reports directory for Markdown links."""

    return Path(os.path.relpath(path.resolve(), REPORT_ROOT.resolve())).as_posix()


def write_json(path: Path, payload: Any) -> None:
    """Write stable JSON."""

    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(json_ready(payload), indent=2, sort_keys=True) + "\n", encoding="utf-8")


def write_csv(path: Path, rows: list[dict[str, Any]]) -> None:
    """Write CSV rows using unioned field names."""

    fields = sorted({key for row in rows for key in row})
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(handle, fieldnames=fields)
        writer.writeheader()
        writer.writerows(rows)


def format_value(value: Any) -> str:
    """Return a readable Markdown cell value."""

    if value is None:
        return "missing"
    if isinstance(value, float):
        return f"{value:.6g}"
    if value == "":
        return "missing"
    return str(value)


def card_markdown(card_payload: dict[str, Any]) -> str:
    """Return one human-readable benchmark-card Markdown body."""

    result = card_payload["result"]
    pipeline = card_payload["pipeline"]
    lines = [
        f"# Benchmark Card: {result['pipeline_id']}",
        "",
        "## Status",
        f"- Case: `{result['case_id']}`",
        f"- Readiness: `{result['readiness']}`",
        f"- Units status: `{result['units_status']}`",
        f"- Recommended use: `{pipeline['recommended_use']}`",
        "- Non-final: true",
        "- Manuscript-ready: false",
        "",
        "## Stage Metrics",
        "",
        "| stage | available | position error [m] | sync error [ns] | missing reason |",
        "|---|---:|---:|---:|---|",
    ]
    for stage in ("initialization", "step_a", "step_b", "step_c"):
        metrics = result[stage]
        lines.append(
            "| "
            + " | ".join(
                [
                    stage,
                    str(metrics["available"]),
                    format_value(metrics["pos_error_m"]),
                    format_value(metrics["sync_error_ns"]),
                    metrics["missing_reason"] or "",
                ]
            )
            + " |"
        )
    lines.extend(
        [
            "",
            "## Truth Use",
            result["truth_use"]["summary"],
            "",
            "## Safe Claims",
            *[f"- {claim}" for claim in card_payload["safe_claims"]],
            "",
            "## Unsafe Claims",
            *[f"- {claim}" for claim in card_payload["unsafe_claims"]],
            "",
            "## Next Action",
            card_payload["recommended_next_action"],
        ]
    )
    return "\n".join(lines) + "\n"


def write_manifest(output_root: Path, payload: dict[str, Any]) -> None:
    """Write benchmark-card manifest files."""

    manifest = {
        **NON_FINAL_FLAGS,
        "generated_at_utc": utc_now(),
        "output_root": repo_rel(output_root),
        "case_id": payload["case_id"],
        "pipeline_ids": payload["pipeline_ids"],
        "script": "scripts/run_standard_benchmark_cards.py",
        "notes": [
            "Primary standard benchmark-card fingerprint only.",
            "Unavailable adapters are included with explicit missing reasons.",
            "No manuscript figures or broad sweeps are generated by this script.",
        ],
    }
    write_json(output_root / "PIPELINE_MANIFEST.json", manifest)
    lines = [
        "# Pipeline Manifest",
        "",
        f"- Generated: `{manifest['generated_at_utc']}`",
        f"- Output root: `{manifest['output_root']}`",
        f"- Case: `{manifest['case_id']}`",
        "- Artifact status: non-final benchmark-card output",
        "- Manuscript-ready: false",
        "",
        "## Pipelines",
        *[f"- `{pipeline_id}`" for pipeline_id in payload["pipeline_ids"]],
        "",
        "## Notes",
        *[f"- {note}" for note in manifest["notes"]],
    ]
    (output_root / "PIPELINE_MANIFEST.md").write_text("\n".join(lines) + "\n", encoding="utf-8")


def write_reports(output_root: Path, cards_payload: list[dict[str, Any]], raw_rows: list[dict[str, Any]], summary_rows: list[dict[str, Any]]) -> None:
    """Write human-readable implementation and benchmark reports."""

    implemented = [row["pipeline_id"] for row in summary_rows if row["adapter_status"] == "adapter_available"]
    unavailable = [row["pipeline_id"] for row in summary_rows if row["adapter_status"] != "adapter_available"]
    package_native = next((row for row in raw_rows if row["pipeline_id"] == "package_native_c7"), None)
    benchmark_json = {
        **NON_FINAL_FLAGS,
        "generated_at_utc": utc_now(),
        "benchmark_execution_run": True,
        "case_id": cards_payload[0]["case"]["case_id"] if cards_payload else "",
        "output_root": repo_rel(output_root),
        "implemented_adapters": implemented,
        "planned_unavailable_adapters": unavailable,
        "raw_csv": repo_rel(output_root / "raw.csv"),
        "summary_csv": repo_rel(output_root / "summary.csv"),
        "benchmark_cards_json": repo_rel(output_root / "benchmark_cards.json"),
        "primary_standard_results": raw_rows,
        "summary": summary_rows,
    }
    write_json(REPORT_ROOT / "NORMALIZED_STANDARD_BENCHMARK_REPORT.json", benchmark_json)

    md_lines = [
        "# Normalized Standard Benchmark Report",
        "",
        "## Executive Summary",
        f"- Benchmark execution run: yes",
        f"- Case: `{benchmark_json['case_id']}`",
        f"- Output root: [{benchmark_json['output_root']}]({reports_rel(output_root)})",
        "- Artifact status: non-final benchmark-card output, not manuscript-ready.",
        "",
        "## Adapter Status",
        "",
        "| pipeline | adapter status | available stages | missing reasons |",
        "|---|---|---|---|",
    ]
    for row in summary_rows:
        md_lines.append(
            f"| `{row['pipeline_id']}` | `{row['adapter_status']}` | {row['available_stages'] or 'none'} | {row['missing_reasons'] or ''} |"
        )
    md_lines.extend(
        [
            "",
            "## Primary Standard-Case Results",
            "",
            "| pipeline | Step A pos [m] | Step B pos [m] | Step C pos [m] | Step A sync [ns] | Step B sync [ns] | Step C sync [ns] |",
            "|---|---:|---:|---:|---:|---:|---:|",
        ]
    )
    for row in raw_rows:
        md_lines.append(
            "| "
            + " | ".join(
                [
                    f"`{row['pipeline_id']}`",
                    format_value(row["step_a_pos_error_m"]),
                    format_value(row["step_b_pos_error_m"]),
                    format_value(row["step_c_pos_error_m"]),
                    format_value(row["step_a_sync_error_ns"]),
                    format_value(row["step_b_sync_error_ns"]),
                    format_value(row["step_c_sync_error_ns"]),
                ]
            )
            + " |"
        )
    if package_native:
        md_lines.extend(
            [
                "",
                "## Step B vs Step C",
                (
                    "- `package_native_c7`: Step C "
                    + ("improves" if package_native["step_c_improves_position_over_step_b"] else "does not improve")
                    + " position over Step B and "
                    + ("improves" if package_native["step_c_improves_sync_over_step_b"] else "does not improve")
                    + " synchronization over Step B on this one primary card."
                ),
            ]
        )
    md_lines.extend(
        [
            "",
            "## Next Action",
            "Implement the controlled Step B and legacy-surgical adapter boundaries, then rerun this same primary benchmark-card command.",
        ]
    )
    (REPORT_ROOT / "NORMALIZED_STANDARD_BENCHMARK_REPORT.md").write_text("\n".join(md_lines) + "\n", encoding="utf-8")

    implementation_json = {
        **NON_FINAL_FLAGS,
        "generated_at_utc": utc_now(),
        "schema_layer_present": True,
        "simulations_run": "primary_standard_single_card_only",
        "broad_sweeps_run": False,
        "figures_generated": False,
        "implemented_adapters": implemented,
        "planned_unavailable_adapters": unavailable,
        "next_step": "Implement remaining safe adapters against the same benchmark-card runner.",
    }
    write_json(REPORT_ROOT / "NORMALIZED_BENCHMARK_ADAPTER_IMPLEMENTATION_REPORT.json", implementation_json)
    implementation_md = [
        "# Normalized Benchmark Adapter Implementation Report",
        "",
        "## Executive Summary",
        "- Implemented the first executable benchmark-card adapter layer.",
        "- Executable adapter: `package_native_c7`.",
        "- Planned/unavailable adapters preserve explicit missing metrics.",
        "- No manuscript figures, broad sweeps, notebook execution, or final results were generated.",
        "",
        "## Implemented Adapters",
        *[f"- `{pipeline_id}`" for pipeline_id in implemented],
        "",
        "## Planned But Unavailable",
        *[f"- `{pipeline_id}`" for pipeline_id in unavailable],
        "",
        "## Output",
        f"- Benchmark report: [{repo_rel(REPORT_ROOT / 'NORMALIZED_STANDARD_BENCHMARK_REPORT.md')}](NORMALIZED_STANDARD_BENCHMARK_REPORT.md)",
        f"- Benchmark cards: [{repo_rel(output_root / 'benchmark_cards.json')}]({reports_rel(output_root / 'benchmark_cards.json')})",
        "",
        "## Next Step",
        implementation_json["next_step"],
    ]
    (REPORT_ROOT / "NORMALIZED_BENCHMARK_ADAPTER_IMPLEMENTATION_REPORT.md").write_text(
        "\n".join(implementation_md) + "\n",
        encoding="utf-8",
    )


def write_outputs(output_root: Path, cards_payload: list[dict[str, Any]], *, force: bool) -> None:
    """Write benchmark-card output files."""

    if output_root.exists() and force:
        shutil.rmtree(output_root)
    elif output_root.exists() and any(output_root.iterdir()):
        raise FileExistsError(f"Output root already exists and is not empty: {output_root}. Use --force.")
    output_root.mkdir(parents=True, exist_ok=True)
    cards_root = output_root / "cards"
    cards_root.mkdir(parents=True, exist_ok=True)

    raw_rows = []
    summary_rows = []
    for payload in cards_payload:
        raw_rows.append(payload["_raw_row"])
        summary_rows.append(payload["_summary_row"])
        card_payload = {key: value for key, value in payload.items() if not key.startswith("_")}
        pipeline_id = card_payload["result"]["pipeline_id"]
        write_json(cards_root / f"{pipeline_id}.json", card_payload)
        (cards_root / f"{pipeline_id}.md").write_text(card_markdown(card_payload), encoding="utf-8")

    clean_payload = [{key: value for key, value in payload.items() if not key.startswith("_")} for payload in cards_payload]
    write_json(output_root / "benchmark_cards.json", {"cards": clean_payload, **NON_FINAL_FLAGS})
    write_csv(output_root / "raw.csv", raw_rows)
    write_csv(output_root / "summary.csv", summary_rows)
    write_manifest(
        output_root,
        {
            "case_id": clean_payload[0]["case"]["case_id"] if clean_payload else "",
            "pipeline_ids": [payload["result"]["pipeline_id"] for payload in clean_payload],
        },
    )
    write_reports(output_root, clean_payload, raw_rows, summary_rows)


def build_payloads(case_id: str, selected_pipeline_ids: list[str] | None) -> list[dict[str, Any]]:
    """Run benchmark cards and return serializable payloads with flattened rows."""

    cards = run_benchmark_cards(case_id=case_id, selected_pipeline_ids=selected_pipeline_ids)
    payloads = []
    for card in cards:
        payload = card.to_dict()
        payload["_raw_row"] = benchmark_card_row(card)
        payload["_summary_row"] = summary_row(card)
        payloads.append(payload)
    return payloads


def print_plan(case_id: str, selected_pipeline_ids: list[str] | None) -> None:
    """Print a no-execution benchmark-card plan."""

    print(f"Standard benchmark-card plan for {case_id}")
    for row in plan_rows(case_id=case_id, selected_pipeline_ids=selected_pipeline_ids):
        print(
            " - "
            + row["pipeline_id"]
            + f" [{row['adapter_status']}] readiness={row['readiness']} units={row['units_status']}"
        )


def parse_args() -> argparse.Namespace:
    """Parse command-line options."""

    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--list-plan", action="store_true", help="List planned cards without running adapters.")
    parser.add_argument("--run", action="store_true", help="Run implemented adapters and write cards.")
    parser.add_argument("--pipeline-id", action="append", dest="pipeline_ids", help="Pipeline ID to include. May be repeated.")
    parser.add_argument("--case-id", default=PRIMARY_STANDARD_CASE_ID, help="Standard case ID to run.")
    parser.add_argument("--output-root", default=str(DEFAULT_OUTPUT_ROOT), help="Output root for --run.")
    parser.add_argument("--force", action="store_true", help="Overwrite output root if it exists.")
    return parser.parse_args()


def main() -> int:
    """CLI entrypoint."""

    args = parse_args()
    selected_pipeline_ids = args.pipeline_ids
    if args.list_plan or not args.run:
        print_plan(args.case_id, selected_pipeline_ids)
        if not args.run:
            return 0
    payloads = build_payloads(args.case_id, selected_pipeline_ids)
    write_outputs(Path(args.output_root), payloads, force=bool(args.force))
    print(f"Wrote benchmark cards to {Path(args.output_root)}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
